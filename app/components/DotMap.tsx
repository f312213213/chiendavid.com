const ROWS = MAP.length;

interface DotMapProps {
  lat?: number;
  lng?: number;
  pins?: { lat: number; lng: number }[];
  className?: string;
}

export default function DotMap({ lat, lng, pins, className = '' }: DotMapProps) {
  const cols = MAP[0].length;
  const gap = 2;
  const r = 0.6;
  const w = cols * gap;
  const h = ROWS * gap;

  // Build pin list from either single lat/lng or pins array
  const allPins = pins
    ? pins
    : lat != null && lng != null
      ? [{ lat, lng }]
      : [];

  return (
    <svg
      viewBox={`0 0 ${w} ${h}`}
      className={className}
      aria-hidden="true"
      fill="currentColor"
    >
      {MAP.map((line, row) =>
        [...line].map((ch, col) =>
          ch === '#' ? (
            <circle
              key={`${row}-${col}`}
              cx={col * gap + gap / 2}
              cy={row * gap + gap / 2}
              r={r}
              opacity={0.18}
            />
          ) : null
        )
      )}
      {allPins.map((pin, i) => {
        const px = ((pin.lng + 180) / 360) * cols * gap + gap / 2;
        const py = ((90 - pin.lat) / 180) * ROWS * gap + gap / 2;
        return (
          <g key={i}>
            <circle cx={px} cy={py} r={3.5} className="fill-accent" opacity={0.18} />
            <circle cx={px} cy={py} r={1.5} className="fill-accent" />
          </g>
        );
      })}
    </svg>
  );
}
